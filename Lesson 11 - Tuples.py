#Tuples are immutable, which means that they can't be changed
#Tuples are indexed starting at 0. 
coordinate = (10, 25) #if you wish to create a list, use brackets[].
#Coordinates    0 , 1 ----- Actual coordinate

print(coordinate[1]) #prints the 1 coordinate. which is 25. 

#you can also make a list of tuples. but remember, the tuples do not change. 
#below is an example of a list of tuples 
Coordinates = [(25,20) , (100,500) , (45,500)]
#Coordinates      0    ,     1     ,    2 ----- Actual coordinates
print(Coordinates[0]) #prints the first tuple at the 0 coordinates. 
